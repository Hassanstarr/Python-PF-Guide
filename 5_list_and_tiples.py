# List: ordered, changeable collection
fruits = ["apple", "banana", "mango"]
print(fruits)

fruits.append("orange")
print(fruits)

fruits.remove("banana")
print(fruits)

first_fruit = fruits[0]
print(first_fruit)
print()

# Nested List 
matrix = [[1,2,3],[4,5,6]]
print(matrix)
print()

# Tuple: immutable collection
point = (2, 4)
x_coord = point[0]
print(x_coord)